public class LiftedPoint {
    private Point point;
    private boolean upwards;

    public LiftedPoint(Point point, boolean upwards) {
        this.point = point;
        this.upwards = upwards;
    }

    public Point getPoint() {
        return point;
    }

    public boolean isUpwards() {
        return upwards;
    }

//fnfkjgk
}

